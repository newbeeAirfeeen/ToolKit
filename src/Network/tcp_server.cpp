#include "tcp_server.hpp"

tcp_server::~tcp_server(){
    stop();
}

void tcp_server::stop(){
    for(auto& acceptor_ : acceptors){
        acceptor_->close();
    }
    event_poller_pool::Instance().for_each([](){
        //清空所有会话
        get_session_helper().clear();
    });
}
